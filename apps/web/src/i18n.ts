const STORAGE_KEY = 'nemsalon-locale';

export type Locale = 'da' | 'en';
export type SupportedLocale = Locale;

const copyEn = copy;

const copyByLocale: Record<Locale, CopyType> = {
  da: copy,
  en: copyEn,
};

export function resolveLocale(locale: string): Locale {
  return locale?.toLowerCase().startsWith('da') ? 'da' : 'en';
}

export function getStoredLocale(): Locale {
  if (typeof window === 'undefined') return 'en';
  const stored = localStorage.getItem(STORAGE_KEY);
  return stored === 'da' ? 'da' : 'en';
}

export function setStoredLocale(locale: string): void {
  if (typeof window === 'undefined') return;
  localStorage.setItem(STORAGE_KEY, locale === 'da' ? 'da' : 'en');
}

export type CopyType = typeof copy;

export function getCopy(locale?: string): CopyType {
  const resolved = resolveLocale(locale || getStoredLocale());
  return copyByLocale[resolved] ?? copy;
}
